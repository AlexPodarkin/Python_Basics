# В Python для получения доступа файлу используют функцию open().

print("открыть что бы дописать файл")
a = open('data.txt', 'a', encoding='utf-8')
a.write("Текст файла \n")
a.close()

print("\nоткрыть что бы прочитать")
r = open('data.txt', 'r', encoding='utf-8')
print(list(r))
r.close()

print("\nоткрыть что бы переписать файл, символ '+' означает что я могу так-же читать файл ")
w = open('data.txt', 'w+', encoding='utf-8')
w.write("Удалил содержимое и записал \n")
w.write("Текст файла \n")
w.close()

